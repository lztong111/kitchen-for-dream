async function ensureSystemUser(): Promise<number> {
  const existing = db.select().from(users).where(eq(users.username, "system")).get();
  if (existing) {
    console.log("System user already exists, id:", existing.id);
    return existing.id;
  }
  const hashedPassword = await bcrypt.hash("system123456", 10);
  const result = db.insert(users).values({
    username: "system",
    password: hashedPassword,
    avatar: null,
  }).run();
  console.log("Created system user, id:", result.lastInsertRowid);
  return Number(result.lastInsertRowid);
}

function clearExistingData() {
  const sqlite = getRawDb();
  console.log("Clearing existing dish data...");
  sqlite.pragma("foreign_keys = OFF");
  db.delete(tags).run();
  db.delete(dish_ingredients).run();
  db.delete(steps).run();
  db.delete(dishes).run();
  sqlite.pragma("foreign_keys = ON");
  console.log("Cleared existing dish data.");
}

async function seed() {
  console.log("=== Starting precise dish seeding ===");
  console.log(`Total dishes to insert: ${ALL_DISHES.length}`);

  const userId = await ensureSystemUser();
  clearExistingData();

  const sqlite = getRawDb();
  sqlite.pragma("foreign_keys = OFF");

  let inserted = 0;
  for (const dish of ALL_DISHES) {
    const dishResult = db.insert(dishes).values({
      user_id: userId,
      name: dish.name,
      description: dish.desc,
      image_url: null,
      category_id: dish.catId,
      cook_time: dish.time,
      difficulty: dish.diff,
      servings: dish.serv,
    }).run();
    const dishId = Number(dishResult.lastInsertRowid);

    for (let i = 0; i < dish.steps.length; i++) {
      db.insert(steps).values({
        dish_id: dishId,
        step_number: i + 1,
        description: dish.steps[i].text,
        image_url: null,
      }).run();
    }

    for (const ing of dish.ings) {
      if (ing.id > 0) {
        db.insert(dish_ingredients).values({
          dish_id: dishId,
          ingredient_id: ing.id,
          amount: ing.amount,
          unit: ing.unit,
        }).run();
      }
    }

    for (const tagName of dish.tagList) {
      db.insert(tags).values({
        dish_id: dishId,
        name: tagName,
      }).run();
    }

    inserted++;
    if (inserted % 50 === 0) {
      console.log(`Progress: ${inserted}/${ALL_DISHES.length} dishes inserted`);
    }
  }

  sqlite.pragma("foreign_keys = ON");

  console.log(`=== Seeding complete! Inserted ${inserted} dishes ===`);

  const catCounts: Record<number, number> = {};
  for (const dish of ALL_DISHES) {
    catCounts[dish.catId] = (catCounts[dish.catId] || 0) + 1;
  }
  console.log("Distribution by category:");
  const catNames: Record<number, string> = {
    17: "凉菜", 18: "热菜", 19: "汤羹", 20: "主食", 21: "面食", 22: "甜点",
    23: "饮品", 24: "烧烤", 25: "鸡肉", 26: "鱼肉", 27: "牛肉", 28: "猪肉",
    29: "羊肉", 30: "海鲜", 31: "蔬菜", 32: "豆制品", 33: "减脂餐",
  };
  for (const [catId, count] of Object.entries(catCounts).sort((a, b) => Number(a[0]) - Number(b[0]))) {
    console.log(`  ${catNames[Number(catId)] || catId}: ${count} dishes`);
  }
}

